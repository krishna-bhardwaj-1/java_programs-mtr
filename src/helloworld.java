public class helloworld {
    public static void main(String... args) {
        System.out.println("my name is krishna bhardwaj");
        System.out.printf("%s%n","Hello \nworld\n");
        System.out.printf("mohit%nsuresh%nramesh%n");
        System.out.printf("%b",true);
        System.out.printf("%B%n",false);
        System.out.printf("%B%n",5.3);
        System.out.printf("%b%n","random text");
        System.out.printf("%s%n","suresh");
        System.out.printf("%S%n","suresh");
        System.out.printf("%15s%n","suresh");
        System.out.printf("'%-10s'%n","suresh");
        System.out.printf("'%2.4s'%n","Hi there is");
        System.out.printf("%C%n",'k');
        System.out.printf("%04d%n",4);
    }
}
