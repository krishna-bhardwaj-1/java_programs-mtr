package string;

public class operations {
    public static void main(String[] args) {
        //concatenation
        String a = "RAM";
        String b ="SHYAM";
        System.out.println(a+" "+b);
        //length
        String c ="RAHUL";
        System.out.println(c.length());
        char ch = c.charAt(0);
        System.out.println(ch);
        //to uppercase/lowercase
        System.out.println(c.toLowerCase());
    }
}
